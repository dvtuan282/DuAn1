    private void showTableNhanVien() {
        nhanVien = (ArrayList<NhanVien_model>) nhanVienService.selectNhanVien();
        model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        for (NhanVien_model nhanVien : nhanVien) {
            String gioiTinh, vaiTro, trangThai;
//            Giới tính
            if (nhanVien.getGioiTinh() == 0) {
                gioiTinh = "Nam";
            } else {
                gioiTinh = "Nữ";
            }
//            Vai trò
            if (nhanVien.getVaiTro() == 0) {
                vaiTro = "Quản lý";
            } else {
                vaiTro = "Nhân viên";
            }
//            Trạng thái
            if (nhanVien.getTrangThai() == 0) {
                trangThai = "Hoạt động";
            } else {
                trangThai = "Ngừng hoạt động";
            }
//            add dữ liệu vào bảng
            model.addRow(new Object[]{nhanVien.getSDT(), nhanVien.getHoTen(), nhanVien.getDiaChi(), nhanVien.getEmail(), nhanVien.getNgaySinh(), gioiTinh, vaiTro, trangThai});
        }
    }

