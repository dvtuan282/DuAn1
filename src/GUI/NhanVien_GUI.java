    private void fileAnh() {
        JFileChooser filechooser = new JFileChooser("C:\\\\Users\\\\tuan0\\\\OneDrive\\\\Máy tính\\\\Java_core\\\\DUAN_BANHANG\\\\src\\\\image");
        FileNameExtensionFilter imageFilter = new FileNameExtensionFilter("hinh anh", "jpg", "jpeg", "png", "webp");
        filechooser.setFileFilter(imageFilter);
        int x = filechooser.showDialog(this, "Chọn Hình Ảnh");
        if (x == JFileChooser.APPROVE_OPTION) {
            File f = filechooser.getSelectedFile();
            srtAnh = f.getAbsolutePath();
            txtAnh.removeAll();
            setImages(srtAnh);
        }
    }

    public void setImages(String image) {
        if (image == null) {
            image = "src/icon/logo_blue.png";
        }
        ImageIcon images = new ImageIcon(image);
        Image im = images.getImage();
        ImageIcon icon = new ImageIcon(im.getScaledInstance(txtAnh.getWidth(), txtAnh.getHeight(), im.SCALE_SMOOTH));
        txtAnh.setText(null);
        txtAnh.setIcon(icon);
    }

